package week_1;
public class Addition {
    public static void main(String[] args) {
        int firstNumber = 5;
        int secondNumber = 4;
        int additionResult = firstNumber+secondNumber;
        System.out.println(firstNumber +" + "+ secondNumber+" = "+ additionResult);
    }
}
